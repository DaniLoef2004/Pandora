                                <h1>Cookieverklaring</h1>
                                <p>Onze website maakt gebruik van cookies, wij vinden het belangrijk u te informeren over het gebruik van cookies op onze website omdat deze privacygevoelige informatie kan bewaren.</p>
                                <h2>Wat zijn cookies?</h2>
                                <p>Cookies zijn kleine tekstbestanden met informatie erop. Bij uw bezoek aan onze website plaatsen wij cookies op uw computer, tablet of smartphone indien uw browserinstellingen dit toelaten.</p>
                                <p>Cookies noteren:</p>
                                <ul>
                                    <li>Dat u bent ingelogd op een site.</li>
                                    <li>Uw voorkeursinstellingen (als deze er zijn), zoals lettergrootte of taal.</li>
                                    <li>Uw surfgedrag.</li>
                                    <li>Uw interesses.</li>
                                </ul>
                                <p>Bij een volgend bezoek, zorgen de cookies ervoor dat u herkend wordt. Cookies bepalen onder andere dat u bij een bezoek niet dezelfde informatie ontvangt of herhaaldelijk in hoeft te voeren. Ook worden er cookies per bezoek opgeslagen om te kijken welke onderdelen van de website er bezocht zijn. Daarnaast kunnen wij door cookies (indien u daar toestemming voor geeft) informatie en aanbiedingen op de website afstemmen op uw voorkeuren.</p>
                                <p>Een deel van de cookies die wij plaatsten of laten plaatsen gebruiken wij voor de functionaliteit van de website of om het gebruik van onze website te analyseren. Hiervoor is geen toestemming nodig. Voor de cookies die uw bezoekgedrag bijhouden en uw interesses onthouden, hebben wij wel uw toestemming nodig, welke u kunt aangeven via onze cookiemelding op onze website.</p>
                                <p>Indien u op onze cookiemelder op de tekst &ldquo;meer info &rdquo;klikt, krijgt u een gespecificeerd overzicht per cookiesoort van onze website.</p>
                                <p>Externe bedrijven kunnen ook cookies plaatsen via onze website.</p>
                                <p>Deze cookieverklaring geldt ook voor andere bestanden die met vergelijkbare technieken gegevens opslaan of uitlezen bij uw websitebezoek (denk bijvoorbeeld aan Javascript, tags etc.).</p>
                                <h2>Welke soorten cookies gebruiken wij?</h2>
                                <p>
                                    <strong>Noodzakelijke cookies/ Functionele cookies</strong>
                                </p>
                                <p>Voor deze cookies is uw toestemming niet nodig.</p>
                                <p>Deze cookies zijn noodzakelijk voor een goede werking van de website, deze worden altijd geplaatst (indien uw browserinstellingen dit toelaten).</p>
                                <p>
                                    <strong>Analytic cookies</strong>
                                </p>
                                <p>Via onze website worden cookies geplaatst van het Amerikaanse bedrijf Google, dit zijn Analytic cookies. Wij gebruiken deze dienst van Google om bij te houden hoe bezoekers onze website gebruiken. Met deze informatie kunnen wij bijvoorbeeld onze website verbeteren of gerichter informatie aanbieden.</p>
                                <p>Google kan deze informatie alleen aan derden verschaffen indien zij hier wettelijk toe worden verplicht of wanneer derde namens Google de informatie dienen te verwerken. Hier hebben wij geen invloed op.</p>
                                <p>Het soort informatie dat Google verzameld met Analytic cookies, is zonder privacygevoelige informatie. Google bewaart de verzamelde informatie op servers in de Verenigde Staten, waarbij zij gehouden zijn aan Privacy-Shield regels van Europa.</p>
                                <p>Wij hebben met Google een verwerkersovereenkomst gesloten.</p>
                                <p>Google Ads (Adwords) worden gebruikt om vergelijkingen te maken tussen Google en Analytics. Hiervoor wordt uw IP-adres gebruikt. Google Ads gegevens worden 30 dagen bewaard door Google.</p>
                                <p>Een uitgebreidere vorm van Adwords houdt bij hoeveel en welke advertenties er aan u getoond zijn. Daarmee kan Google nagaan of u op een advertentie heeft geklikt. Hiervoor wordt ook uw IP-adres gebruikt. Google mag deze gegevens 180 dagen bewaren.</p>
                                <p>
                                    <strong>Tracking cookies/ Marketing cookies</strong>
                                </p>
                                <p>Daarnaast gebruiken wij tracking cookies voor gerichte bezoekersinformatie, om de website gebruikersvriendelijker en persoonlijker te maken.</p>
                                <p>De informatie die wordt opgeslagen wordt gebruikt om u te identificeren en om uw gegevens te gebruiken voor gerichte reclame. Aan de hand van pagina &rsquo;s die u bezoekt kunnen wij opmaken waarin u ge &iuml;nteresseerd bent. Deze cookies worden vaak geplaatst via derden (bijvoorbeeld Facebook, LinkedIn, Twitter e.d.), waardoor deze cookies ook vaak Third Party cookies worden genoemd.</p>
                                <p>Google Adwords Remarketing cookies worden bewaard om uw bezoekersgedrag en profiel van u op te bouwen, zodat u alleen op uw interesses advertenties aangeboden worden. Hier wordt uw IP-adres voor gebruikt. Google mag deze gegevens 540 dagen bewaren.</p>
                                <p>Doubleclick cookies slaan cookies op om advertenties te verbeteren. Zij zorgen ervoor dat u gepersonaliseerde advertenties van Google aangeboden krijgt.</p>
                                <p>&lsquo;Social Media &rsquo;cookies worden opgeslagen om webpagina &rsquo;s of onderdelen te kunnen promoten of delen op sociale netwerken zoals Facebook, YouTube, Twitter, Instagram en LinkedIn. Alleen de Sociale Mediakanalen die genoemd worden in onze privacyverklaring en op onze website zijn hier van toepassing.</p>
                                <p>De &lsquo;Social Media &rsquo;kanalen werken met stukjes code die van sociale media zelf afkomstig zijn. Door middel van deze code worden cookies geplaatst. Wij hebben daar geen invloed op.</p>
                                <p>Het is dus mogelijk dat andere partijen naast ons cookies plaatsen op uw apparaat bij een bezoek aan onze website. Wij verwijzen u naar de websites van deze partijen indien u meer over de cookie-opslag van deze partijen wilt weten.</p>
                                <p>
                                    <strong>Cookies verwijderen/ instellingen aanpassen</strong>
                                </p>
                                <p>Wilt u geen of beperkte cookies?</p>
                                <p>Dan kunt u verschillende dingen doen:</p>
                                <ol>
                                    <li>U kunt in onze cookiemelding per cookiesoort de door u ongewenste cookies aan- of uitvinken of via de onderstaande knop &lsquo;instellingen &rsquo;de cookie instellingen aanpassen.</li>
                                    <li>
                                        U kunt in uw browser de opslag van cookies uitschakelen of opgeslagen cookies verwijderen, maar dit kan leiden tot verminderde functionaliteit of vertraging in de website. . Hoe u de browserinstellingen kunt aanpassen verschilt per browser. U kunt hiervoor de &#39;Help-functie &#39;in uw browser raadplegen. Alle geplaatste cookies worden bewaard zo lang als noodzakelijk is voor het doel waarvoor ze verzameld zijn.<br/>Op uw mobiele telefoon kunt u in de instellingen het accepteren van cookies aan- en uitzetten.
                                    </li>
                                    <li>U kunt met bepaalde programma &rsquo;s (bijvoorbeeld CCleaner) uw apparaat makkelijk automatisch of handmatig cookies laten verwijderen.</li>
                                </ol>
                                <h2>Up to date</h2>
                                <p>Wij proberen deze cookieverklaring zo up-to-date mogelijk te houden. Vanwege de snelle ontwikkelingen op het internet is het mogelijk dat er cookies worden geplaatst die nog niet worden genoemd in de informatie die opgevraagd kan worden in onze cookiemelding. Wij zullen regelmatig deze verklaring controleren en daarop aanpassen.</p>
